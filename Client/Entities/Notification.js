export class Notification {
	constructor(
		id,
		username,
		firstname,
		lastname,
		productname,
		productid,
		price,
		barter
	) {
		this.id = id;
		this.username = username;
		this.firstName = firstname;
		this.lastName = lastname;
		this.productName = productname;
		this.productId = productid;
		this.price = price;
		this.barter = barter;
	}
}
